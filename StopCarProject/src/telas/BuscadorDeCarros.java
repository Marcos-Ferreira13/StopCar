    private void buscaCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscaCategoriaMouseClicked
        String s = (String) categoriaBox.getSelectedItem();
        System.out.println(s);

        if(s.equals("PICAPES")){
            ArrayList<Carro> carrosEncontrados = new ArrayList<>();
            carrosEncontrados = sistema.buscaCarroPorCategoria(CategoriaCarro.PICAPE);
            JOptionPane.showMessageDialog(null,carrosEncontrados);
        }
        if(s.equals("SUV")){
            ArrayList<Carro> carrosEncontrados = new ArrayList<>();
            carrosEncontrados = sistema.buscaCarroPorCategoria(CategoriaCarro.PICAPE);
            JOptionPane.showMessageDialog(null,carrosEncontrados);
        }
        if(s.equals("CONVERSIVEIS")){
            ArrayList<Carro> carrosEncontrados = new ArrayList<>();
            carrosEncontrados = sistema.buscaCarroPorCategoria(CategoriaCarro.PICAPE);
            JOptionPane.showMessageDialog(null,carrosEncontrados);
        }

    }//GEN-LAST:event_buscaCategoriaMouseClicked

